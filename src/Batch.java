    private void faculty() {
        try {
            Vector v = Databasemanager.getFaculty();
            factcombo.removeAllItems();
            for (int i = 0; i < v.size(); i++) {
                FacultyBean bean = (FacultyBean) v.elementAt(i);
                factcombo.addItem(bean);
            }
        } catch (Exception e) {
        }

    }

    private void department() {
        FacultyBean bean = (FacultyBean) factcombo.getSelectedItem();
        if (bean == null) {
            return;
        }
        try {
            deptcombo.removeAllItems();
            Vector v = Databasemanager.getDepartment(bean.getFacid());
            for (int i = 0; i < v.size(); i++) {
                DepartmentBean bean1 = (DepartmentBean) v.elementAt(i);
                deptcombo.addItem(bean1);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }

    }

    private void program() {
        DepartmentBean bean = (DepartmentBean) deptcombo.getSelectedItem();
        if (bean == null) {
            return;
        }
        try {
            progcombo.removeAllItems();
            Vector v = Databasemanager.getProgram(bean.getDeptId());
            for (int i = 0; i < v.size(); i++) {
                ProgramBean bean1 = (ProgramBean) v.elementAt(i);
                progcombo.addItem(bean1);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }

    private void getbatch() {
        ProgramBean bean = (ProgramBean) progcombo.getSelectedItem();
        if (bean == null) {
            return;
        }
        try {
            Vector v = Databasemanager.getBatch(bean.getProgId());
            batchlist.setListData(v);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
        }
    }

    public void valueChanged(ListSelectionEvent e) {
        BatchBean bean = (BatchBean) batchlist.getSelectedValue();
        if (bean == null) {
            return;
        }
        setbatchid.setText("" + bean.getBatchId());
        setbatchyear.setSelectedItem(bean.getBatchYear());
        String group=Decoder.groupDecode(bean.getGroupDesc());
        setgroup.setSelectedItem(group);
        String shift=Decoder.shiftDecode(bean.getShift());
        setshift.setSelectedItem(shift);
        setremarks.setText("" + bean.getRemarks());

    } //end valuechanger method

    private void Update() {
        BatchBean bean = (BatchBean) batchlist.getSelectedValue();
        if (bean == null) {
            return;
        }

        try {
            String shift=Encoder.shiftEncode((String)setshift.getSelectedItem());
            String group=Encoder.groupEncode((String)setgroup.getSelectedItem());
           
            int rows = Databasemanager.UpdateBatch(bean.getProgID(), bean.getBatchId(),
                 (String)setbatchyear.getSelectedItem(),
                     shift, group, setremarks.getText());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

