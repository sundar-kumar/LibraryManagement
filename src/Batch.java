    private void addrecords() {
        ProgramBean bean = (ProgramBean) progcombo.getSelectedItem();
        if (bean == null) {
            return;
        }

        String batchyear = (String) setbatchyear.getSelectedItem();
        String shift = Encoder.shiftEncode((String) setshift.getSelectedItem());
        String group = Encoder.groupEncode((String) setgroup.getSelectedItem());
        String remarks = setremarks.getText();

        try {
            int rows = Databasemanager.addBatch(bean.getProgId(),
                    batchyear, shift, group, remarks);

        } catch (Exception e) {
        }
    }

    private void DeleteBatch() {
        BatchBean bean = (BatchBean) batchlist.getSelectedValue();
        if (bean == null) {
            return;
        }
        try {
            int row = Databasemanager.deleteBatch(bean.getBatchId());
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

